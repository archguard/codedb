package org.archguard.codedb.metrics.code

class OOMetric {
    fun loc() {
        println("OO Metric LOC")
    }

    // number of classes
    fun noc() {
        println("OO Metric NOC")
    }

    // number of methods
    fun nom() {
        println("OO Metric NOM")
    }

    // number of attributes
    fun noa() {
        println("OO Metric NOA")
    }

    // number of inheritance
    fun noi() {
        println("OO Metric NOI")
    }

    // dit (depth of inheritance tree)
    fun dit() {
        println("OO Metric DIT")
    }
}